def transfer_wallet_to_wallet(sender_wallet_uid: str, receiver_wallet_uid: str, amount: float):
    """Transaction Type: WALLET_TO_WALLET"""
    validate_amount(amount)
    if sender_wallet_uid == receiver_wallet_uid:
        raise ValueError("Sender and receiver wallet cannot be same.")

    conn = None
    cursor = None
    try:
        conn = get_connection()
        conn.start_transaction()
        cursor = conn.cursor(dictionary=True)

        cursor.execute("SELECT balance FROM wallet WHERE wallet_uid = %s FOR UPDATE", (sender_wallet_uid,))
        sender_row = cursor.fetchone()
        if not sender_row:
            raise ValueError("Sender wallet not found.")
        if sender_row["balance"] < amount:
            raise ValueError("Insufficient wallet balance.")

        cursor.execute("SELECT balance FROM wallet WHERE wallet_uid = %s FOR UPDATE", (receiver_wallet_uid,))
        receiver_row = cursor.fetchone()
        if not receiver_row:
            raise ValueError("Receiver wallet not found.")

        cursor.execute(
            "UPDATE wallet SET balance = balance - %s WHERE wallet_uid = %s",
            (amount, sender_wallet_uid),
        )
        cursor.execute(
            "UPDATE wallet SET balance = balance + %s WHERE wallet_uid = %s",
            (amount, receiver_wallet_uid),
        )

        txn = record_transaction(
            cursor,
            sender_wallet_uid,
            receiver_wallet_uid,
            amount,
            "WALLET_TO_WALLET",
            "SUCCESS",
        )
        conn.commit()
        create_pdf_receipt(txn)
        return txn

    except Exception:
        if conn:
            conn.rollback()
        raise
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()


def fetch_recent_transactions(wallet_uid: str):
    """Fetch last 30 minutes transactions for a wallet."""
    conn = None
    cursor = None
    try:
        conn = get_connection()
        cursor = conn.cursor(dictionary=True)
        cursor.execute(
            """
            SELECT txn_id, sender_uid, receiver_uid, amount, txn_type, date_time, status
            FROM transactions
            WHERE (sender_uid = %s OR receiver_uid = %s)
              AND date_time >= NOW() - INTERVAL 30 MINUTE
            ORDER BY date_time DESC
            """,
            (wallet_uid, wallet_uid),
        )
        return cursor.fetchall()
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()



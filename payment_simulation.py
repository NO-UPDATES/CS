MAX_TXN_LIMIT = 99999.0
BANK_UID_PREFIX = "BANK"
INTERNAL_LAYER_UID = "SYSTEM_LAYER"


def get_connection():
    """Create and return a new MySQL connection."""
    return mysql.connector.connect(**DB_CONFIG)


def hash_password(raw_password: str) -> str:
    """Hash a password using SHA-256."""
    return hashlib.sha256(raw_password.encode("utf-8")).hexdigest()


def generate_wallet_uid() -> str:
    """Generate a unique wallet UID using uuid4."""
    return str(uuid.uuid4())


def generate_bank_uid() -> str:
    """Generate a bank UID with random 10-digit number."""
    return f"{BANK_UID_PREFIX}{random.randint(10**9, 10**10 - 1)}"


def generate_txn_id() -> str:
    """Generate a unique transaction ID."""
    return f"TXN-{uuid.uuid4().hex[:16].upper()}"


def create_pdf_receipt(txn: dict):
    """Generate PDF receipt for a successful transaction."""
    filename = f"receipt_{txn['txn_id']}.pdf"
    c = canvas.Canvas(filename, pagesize=A4)
    width, height = A4

    c.setFont("Helvetica-Bold", 16)
    c.drawString(50, height - 50, "Transaction Receipt")

    c.setFont("Helvetica", 12)
    lines = [
        f"Transaction ID: {txn['txn_id']}",
        f"Transaction Type: {txn['txn_type']}",
        f"Sender UID: {txn['sender_uid']}",
        f"Receiver UID: {txn['receiver_uid']}",
        f"Amount: INR {txn['amount']:.2f}",
        f"Date & Time: {txn['date_time']}",
        f"Status: {txn['status']}",
    ]

    y = height - 100
    for line in lines:
        c.drawString(50, y, line)
        y -= 24

    c.setFont("Helvetica-Oblique", 10)
    c.drawString(50, y - 20, "Generated by Layered Payment Simulation System")
    c.save()


def register_user(name: str, username: str, password: str):
    """Register a new user with wallet and bank account."""
    conn = None
    cursor = None
    try:
        wallet_uid = generate_wallet_uid()
        bank_uid = generate_bank_uid()
        pass_hash = hash_password(password)

        conn = get_connection()
        conn.start_transaction()
        cursor = conn.cursor(dictionary=True)

        cursor.execute("SELECT user_id FROM users WHERE username = %s", (username,))
        if cursor.fetchone():
            raise ValueError("Username already exists.")

        # Ensure generated bank UID is unique
        while True:
            cursor.execute("SELECT bank_uid FROM users WHERE bank_uid = %s", (bank_uid,))
            if not cursor.fetchone():
                break
            bank_uid = generate_bank_uid()

        cursor.execute(
            """
            INSERT INTO users (name, username, password_hash, wallet_uid, bank_uid, created_at)
            VALUES (%s, %s, %s, %s, %s, NOW())
            """,
            (name, username, pass_hash, wallet_uid, bank_uid),
        )

        cursor.execute("INSERT INTO wallet (wallet_uid, balance) VALUES (%s, %s)", (wallet_uid, 0.0))
        cursor.execute("INSERT INTO bank_account (bank_uid, balance) VALUES (%s, %s)", (bank_uid, 50000.0))

        conn.commit()
        return {"wallet_uid": wallet_uid, "bank_uid": bank_uid}

    except Exception:
        if conn:
            conn.rollback()
        raise
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()


def authenticate_user(username: str, password: str):
    """Authenticate user by username and hashed password."""
    conn = None
    cursor = None
    try:
        conn = get_connection()
        cursor = conn.cursor(dictionary=True)
        cursor.execute("SELECT * FROM users WHERE username = %s", (username,))
        user = cursor.fetchone()
        if not user:
            return None
        if user["password_hash"] != hash_password(password):
            return None
        return user
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()


def verify_password(username: str, password: str) -> bool:
    """Verify password for sensitive dashboard actions."""
    user = authenticate_user(username, password)
    return user is not None


def get_user_balances(wallet_uid: str, bank_uid: str):
    """Fetch wallet and bank balances."""
    conn = None
    cursor = None
    try:
        conn = get_connection()
        cursor = conn.cursor(dictionary=True)

        cursor.execute("SELECT balance FROM wallet WHERE wallet_uid = %s", (wallet_uid,))
        wallet_row = cursor.fetchone()

        cursor.execute("SELECT balance FROM bank_account WHERE bank_uid = %s", (bank_uid,))
        bank_row = cursor.fetchone()

        wallet_balance = wallet_row["balance"] if wallet_row else 0.0
        bank_balance = bank_row["balance"] if bank_row else 0.0
        return wallet_balance, bank_balance
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()


def record_transaction(cursor, sender_uid: str, receiver_uid: str, amount: float, txn_type: str, status: str):
    """Insert a transaction row and return transaction details."""
    txn_id = generate_txn_id()
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    cursor.execute(
        """
        INSERT INTO transactions (txn_id, sender_uid, receiver_uid, amount, txn_type, date_time, status)
        VALUES (%s, %s, %s, %s, %s, NOW(), %s)
        """,
        (txn_id, sender_uid, receiver_uid, amount, txn_type, status),
    )
    return {
        "txn_id": txn_id,
        "sender_uid": sender_uid,
        "receiver_uid": receiver_uid,
        "amount": amount,
        "txn_type": txn_type,
        "date_time": now,
        "status": status,
    }


def validate_amount(amount: float):
    if amount <= 0:
        raise ValueError("Amount must be positive.")
    if amount > MAX_TXN_LIMIT:
        raise ValueError(f"Maximum allowed per transaction is INR {MAX_TXN_LIMIT:.0f}.")


def transfer_bank_to_wallet(bank_uid: str, wallet_uid: str, amount: float):
    """Transaction Type: BANK_TO_WALLET"""
    validate_amount(amount)
    conn = None
    cursor = None
    try:
        conn = get_connection()
        conn.start_transaction()
        cursor = conn.cursor(dictionary=True)

        cursor.execute("SELECT balance FROM bank_account WHERE bank_uid = %s FOR UPDATE", (bank_uid,))
        bank_row = cursor.fetchone()
        if not bank_row:
            raise ValueError("Bank account not found.")
        if bank_row["balance"] < amount:
            raise ValueError("Insufficient bank balance.")

        cursor.execute("SELECT balance FROM wallet WHERE wallet_uid = %s FOR UPDATE", (wallet_uid,))
        if not cursor.fetchone():
            raise ValueError("Wallet not found.")

        cursor.execute("UPDATE bank_account SET balance = balance - %s WHERE bank_uid = %s", (amount, bank_uid))
        cursor.execute("UPDATE wallet SET balance = balance + %s WHERE wallet_uid = %s", (amount, wallet_uid))

        txn = record_transaction(cursor, bank_uid, wallet_uid, amount, "BANK_TO_WALLET", "SUCCESS")
        conn.commit()
        create_pdf_receipt(txn)
        return txn

    except Exception:
        if conn:
            conn.rollback()
        raise
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()



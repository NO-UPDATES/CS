def fetch_incoming_after(wallet_uid: str, after_datetime: datetime):
    """Fetch incoming wallet-to-wallet transactions after a timestamp."""
    conn = None
    cursor = None
    try:
        conn = get_connection()
        cursor = conn.cursor(dictionary=True)
        cursor.execute(
            """
            SELECT txn_id, sender_uid, receiver_uid, amount, txn_type, date_time, status
            FROM transactions
            WHERE receiver_uid = %s
              AND txn_type = 'WALLET_TO_WALLET'
              AND date_time > %s
            ORDER BY date_time ASC
            """,
            (wallet_uid, after_datetime),
        )
        return cursor.fetchall()
    finally:
        if cursor:
            cursor.close()
        if conn:
            conn.close()



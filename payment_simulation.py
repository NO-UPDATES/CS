    def handle_register(self):
        name = self.reg_name.get().strip()
        username = self.reg_username.get().strip()
        password = self.reg_password.get().strip()

        if not name or not username or not password:
            messagebox.showerror("Input Error", "All fields are required.")
            return

        if len(password) < 4:
            messagebox.showerror("Input Error", "Password must be at least 4 characters.")
            return

        try:
            result = register_user(name, username, password)
            msg = (
                f"Registration successful.\nWallet UID: {result['wallet_uid']}\n"
                f"Bank UID: {result['bank_uid']}\nInitial bank balance: INR 50000"
            )
            messagebox.showinfo("Success", msg)
            self.build_login_screen()
        except Error as e:
            messagebox.showerror("Database Error", str(e))
        except Exception as e:
            messagebox.showerror("Registration Failed", str(e))

    def handle_login(self):
        username = self.login_username.get().strip()
        password = self.login_password.get().strip()

        if not username or not password:
            messagebox.showerror("Input Error", "Username and password are required.")
            return

        try:
            user = authenticate_user(username, password)
            if not user:
                messagebox.showerror("Login Failed", "Invalid username or password.")
                return

            self.current_user = user
            self.last_poll_time = datetime.now()
            self.build_dashboard()
            self.start_notification_polling()
        except Error as e:
            messagebox.showerror("Database Error", str(e))

